class Solution {
public:
    uint32_t reverseBits(uint32_t n) {
        n = ((n & stoul("11111111111111110000000000000000", NULL, 2)) >> 16) |
            ((n & stoul("00000000000000001111111111111111", NULL, 2)) << 16);
        n = ((n & stoul("11111111000000001111111100000000", NULL, 2)) >> 8) |
            ((n & stoul("00000000111111110000000011111111", NULL, 2)) << 8);
        n = ((n & stoul("11110000111100001111000011110000", NULL, 2)) >> 4) |
            ((n & stoul("00001111000011110000111100001111", NULL, 2)) << 4);
        n = ((n & stoul("11001100110011001100110011001100", NULL, 2)) >> 2) |
            ((n & stoul("00110011001100110011001100110011", NULL, 2)) << 2);
        n = ((n & stoul("10101010101010101010101010101010", NULL, 2)) >> 1) |
            ((n & stoul("01010101010101010101010101010101", NULL, 2)) << 1);
        return n;
    }
};