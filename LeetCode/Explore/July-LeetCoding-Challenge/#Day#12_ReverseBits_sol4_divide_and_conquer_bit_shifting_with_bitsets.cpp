class Solution {
public:
    uint32_t reverseBits(uint32_t n) {
        n = ((n & bitset<32>("11111111111111110000000000000000").to_ulong()) >> 16) |
            ((n & bitset<32>("00000000000000001111111111111111").to_ulong()) << 16);
        n = ((n & bitset<32>("11111111000000001111111100000000").to_ulong()) >> 8) |
            ((n & bitset<32>("00000000111111110000000011111111").to_ulong()) << 8);
        n = ((n & bitset<32>("11110000111100001111000011110000").to_ulong()) >> 4) |
            ((n & bitset<32>("00001111000011110000111100001111").to_ulong()) << 4);
        n = ((n & bitset<32>("11001100110011001100110011001100").to_ulong()) >> 2) |
            ((n & bitset<32>("00110011001100110011001100110011").to_ulong()) << 2);
        n = ((n & bitset<32>("10101010101010101010101010101010").to_ulong()) >> 1) |
            ((n & bitset<32>("01010101010101010101010101010101").to_ulong()) << 1);
        return n;
    }
};